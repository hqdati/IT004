        IF EXISTS (
            SELECT I.HOCVI, I.HOCHAM, I.HESO
            FROM inserted AS I
            JOIN GIAOVIEN AS GV
                ON I.HOCVI = GV.HOCVI
                AND I.HOCHAM = GV.HOCHAM
                AND I.HESO = GV.HESO
            GROUP BY I.HOCVI, I.HOCHAM, I.HESO
