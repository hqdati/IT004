        SELECT 1
        FROM inserted AS I
        JOIN deleted AS D
        ON I.MAGV = D.MAGV
        WHERE 
            I.HOCVI <> D.HOCVI OR
            I.HOCHAM <> D.HOCHAM OR
            I.HESO <> D.HESO
    )
