		SELECT GV.HOCVI, GV.HOCHAM, GV.HESO
		FROM GIAOVIEN AS GV
		JOIN inserted
		ON inserted.HOCVI = GV.HOCVI 
			AND inserted.HOCHAM = GV.HOCHAM
