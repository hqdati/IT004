-- HOCVIEN: DELETE
CREATE TRIGGER SISO_HOCVIEN_DELETE
ON HOCVIEN
FOR DELETE
AS 
BEGIN
	-- Xóa học viên ra khỏi lớp
	UPDATE LOP
	SET SISO = SISO - (
		SELECT COUNT(*)
		FROM deleted
		WHERE deleted.MALOP = LOP.MALOP
	)
	WHERE MALOP IN (
		SELECT MALOP
		FROM deleted
	)

	-- Kiểm tra điều kiện
	IF EXISTS (
		SELECT *
		FROM LOP 
		WHERE SISO < 0
			AND MALOP IN (
				SELECT MALOP
				FROM deleted
			)
	)
	BEGIN
		PRINT 'LOP KHONG CO HOC VIEN --> DELETE HOCVIEN KHONG THANH CONG';
		ROLLBACK TRANSACTION;
	END 
	ELSE 
	BEGIN	
		PRINT 'DELETE HOCVIEN THANH CONG';
	END
END

